    public function index()
    {
        if (isset($_GET['page'])) {
            $page = $_GET['page'];
        } else {
            $page = 1;
        }

        $users = User::paginate(10);
        $userCount = User::count();
        $lastpage = ceil($userCount / 10);
        
        return view('admin-users.index', [
            'users'    => $users,
            'page'     => $page,
            'lastpage' => $lastpage
        ]);
    }

